hello_string = "hello"
float_value = 12.0
int_value = 18

if hello_string == "hello":
    print(f"String: {hello_string}")
if isinstance(float_value, float) and float_value == 12.0:
    print(f"Float: {float_value}")
if isinstance(int_value, int) and int_value == 18:
    print(f"Integer: {int_value}")
